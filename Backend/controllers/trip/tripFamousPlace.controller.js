// POST /api/trips/:tripId/places
export const addTripPlace = asyncHandler(async (req, res) => {
  const { tripId } = req.params;
  const userId = req.user._id;

  /* ------------------ TRIP & PERMISSION ------------------ */
  const trip = await Trip.findById(tripId);
  if (!trip) throw new ApiError(404, "Trip not found");

  const allowed = await canManageTripPlaces({ tripId, userId, trip });
  if (!allowed) {
    throw new ApiError(403, "Only Creator, Captain or Planner can add places");
  }

  /* ------------------ SAFE PARSE (FormData) ------------------ */
  const parseIfString = (v) => (typeof v === "string" ? JSON.parse(v) : v);

  const name = req.body.name;
  const category = req.body.category;
  const location = parseIfString(req.body.location);
  const links = parseIfString(req.body.links) || [];

  /* ------------------ BASIC VALIDATION ------------------ */
  if (!name?.trim()) {
    throw new ApiError(400, "Place name is required");
  }

  const allowedCategories = [
    "monument",
    "temple",
    "market",
    "nature",
    "museum",
    "food",
    "other",
  ];

  if (!allowedCategories.includes(category)) {
    throw new ApiError(400, "Invalid place category");
  }

  /* ------------------ LOCATION (OPTIONAL & SAFE) ------------------ */
  const locationPayload = {};

  if (location?.name) {
    locationPayload.name = location.name;
  }

  if (
    location?.coordinates?.lng !== undefined &&
    location?.coordinates?.lat !== undefined
  ) {
    const lng = Number(location.coordinates.lng);
    const lat = Number(location.coordinates.lat);

    if (
      !Number.isNaN(lng) &&
      !Number.isNaN(lat) &&
      lng >= -180 &&
      lng <= 180 &&
      lat >= -90 &&
      lat <= 90
    ) {
      // ✅ ONLY add coordinates if fully valid
      locationPayload.coordinates = {
        type: "Point",
        coordinates: [lng, lat],
      };
    }
  }

  /* ------------------ LINKS (OPTIONAL) ------------------ */
  const sanitizedLinks = Array.isArray(links)
    ? links
        .filter((l) => l?.label && l?.url)
        .map((l) => ({
          label: l.label.trim(),
          url: l.url.trim(),
          type: l.type || "other",
        }))
    : [];

  /* ------------------ IMAGE UPLOAD ------------------ */
  const image = req.file ? await uploadTripPlaceImage(req.file, tripId) : null;

  /* ------------------ CREATE PLACE ------------------ */
  const place = await TripPlace.create({
    trip: tripId,
    name: name.trim(),
    category,
    links: sanitizedLinks,
    location: locationPayload, // 🔥 NEVER partial GeoJSON
    image,
    autoGenerated: false,
  });

  return res
    .status(201)
    .json(new ApiResponse(201, place, "Trip place added successfully"));
});

// DELETE /api/trips/:tripId/places/:placeId
export const deleteTripPlace = asyncHandler(async (req, res) => {
  const { tripId, placeId } = req.params;
  const userId = req.user._id;

  if (!mongoose.Types.ObjectId.isValid(placeId)) {
    throw new ApiError(400, "Invalid place id");
  }

  const trip = await Trip.findById(tripId);
  if (!trip) throw new ApiError(404, "Trip not found");

  const allowed = await canManageTripPlaces({ tripId, userId, trip });
  if (!allowed) {
    throw new ApiError(403, "Only Captain or Planner can delete places");
  }

  const place = await TripPlace.findOne({ _id: placeId, trip: tripId });
  if (!place) throw new ApiError(404, "Place not found");

  if (place.image?.publicId) {
    await cloudinary.uploader.destroy(place.image.publicId);
  }

  await place.deleteOne();

  return res
    .status(200)
    .json(new ApiResponse(200, { placeId }, "Trip place deleted"));
});
