import { Trip } from "../../models/trip/trip.model.js";
import { TripPlace } from "../../models/trip/tripPlace.model.js";
import { ApiError } from "../../utils/ApiError.js";
import { ApiResponse } from "../../utils/ApiResponse.js";
import asyncHandler from "../../utils/asyncHandler.js";
import cloudinary from "../../utils/cloudinary.js";
import getDataUri from "../../utils/datauri.js";
import { optimizeImageBuffer } from "../../utils/sharpImage.js";

import { TripRole } from "../../models/trip/tripRole.model.js";
import mongoose from "mongoose";

const canManageTripPlaces = async ({ tripId, userId, trip }) => {
  // Trip creator always allowed
  if (trip.createdBy.equals(userId)) return true;

  // Check TripRole
  const role = await TripRole.findOne({
    trip: tripId,
    assignedTo: userId,
    roleName: { $in: ["Captain", "Planner"] },
    status: "active",
  }).lean();

  return !!role;
};

const uploadTripPlaceImage = async (file, tripId) => {
  if (!file) return null;

  const optimizedBuffer = await optimizeImageBuffer(file.buffer, {
    maxWidth: 1600,
    maxHeight: 1600,
    quality: 80,
  });

  const uri = getDataUri({
    buffer: optimizedBuffer,
    mimetype: "image/jpeg",
  });

  const uploaded = await cloudinary.uploader.upload(uri.content, {
    folder: `trips/${tripId}/places`,
    resource_type: "image",
    overwrite: false,
    transformation: {
      quality: "auto",
      fetch_format: "auto",
    },
  });

  return {
    url: uploaded.secure_url,
    publicId: uploaded.public_id,
  };
};

